  private readonly contractAddress: string = '0xa6dBa623844cFb72389F1362Cbc7ef5a607e86c2';

  constructor() {
    this.web3 = new Web3(Web3.givenProvider || 'ws://localhost:8545');
  }

  async ngOnInit(): Promise<void> {
    await this.connect();
    await this.loadContract();
    await this.loadAccounts();
    await this.getBalance();
  }

  async connect(): Promise<void> {
    const provider = await detectEthereumProvider();
    if (!provider) throw new Error('No Ethereum provider detected');
    this.web3 = new Web3(provider as any);
    await (provider as any).request({ method: 'eth_requestAccounts' });
  }

  loadContract(): void {
    this.contract = new this.web3.eth.Contract(this.contractABI, this.contractAddress);
  }

  async loadAccounts(): Promise<void> {
    this.accounts = await this.web3.eth.getAccounts();
  }

  async getBalance(): Promise<void> {
    this.balance = await this.contract.methods.balanceOf(this.accounts[0]).call();
  }

  async transferTokens(): Promise<void> {
    const result = await this.contract.methods
      .transfer(this.transferTo, this.transferAmount)
      .send({ from: this.accounts[0] });
    this.transferResult = result ? 'Success' : 'Failed';
    await this.getBalance();
  }
